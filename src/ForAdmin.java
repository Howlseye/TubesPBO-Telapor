    private void tabelLaporanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelLaporanMouseClicked
        // TODO add your handling code here:
        //Ambil baris berapa yang diklik
        int baris = tabelLaporan.getSelectedRow();
        
        //Ambil model tabelnya
        DefaultTableModel model = (DefaultTableModel)tabelLaporan.getModel();
        
        //Ambil data dari baris itu, kolom per kolom
        String id = model.getValueAt(baris, 0).toString();
        String nama = model.getValueAt(baris, 1).toString();
        String deskripsi = model.getValueAt(baris, 3).toString();
        String status = model.getValueAt(baris, 4).toString();
        
        //Tempelkan data tersebut ke textfield dibawah
        txtIDAdmin.setText(id);
        txtNamaAdmin.setText(nama);
        txtDeskripsiAdmin.setText(deskripsi);
        cmbStatusAdmin.setSelectedItem(status); //otomatis memilih status sesuai di combobox
    }//GEN-LAST:event_tabelLaporanMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        //Ambil data dari form inputan admin
        String id = txtIDAdmin.getText();
        String namaBaru = txtNamaAdmin.getText();
        String deskripsiBaru = txtDeskripsiAdmin.getText();
        String statusBaru = cmbStatusAdmin.getSelectedItem().toString();
        
        //cek apakah ada id yang dipilih
        if (id.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Pilih dahulu data pada tabel!");
            return;
        }
        
        try {
            Connection conn = KoneksiDB.configDB();
            //query sql update
            String sql = "UPDATE laporan SET nama_pelapor=?, deskripsi=?, status=? WHERE id=?";
            PreparedStatement pst = conn.prepareStatement(sql);
            
            //isi tanda tanya
            pst.setString(1, namaBaru);
            pst.setString(2, deskripsiBaru);
            pst.setString(3, statusBaru);
            pst.setString(4, id);
            pst.execute();
            JOptionPane.showMessageDialog(this, "Data Berhasil Diupdate!");
            
            tampilkanData();
            bersihkanFormAdmin();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal update: " + e.getMessage());
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        String id = txtIDAdmin.getText();
        if (id.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Pilih dahulu data yang akan dihapus pada tabel!");
            return;
        }
        
        //Menampilkan dialog konfirmasi ya/tidak
        int confirm = JOptionPane.showConfirmDialog(this, 
                "Yakin ingin menghapus laporan ID: " + id + "?", 
                "Konfirmasi Hapus", 
                JOptionPane.YES_NO_OPTION);
        //jika user pilih yes
        if (confirm == 0) {
            try {
                Connection conn = KoneksiDB.configDB();
                String sql = "DELETE FROM laporan WHERE id=?";
                PreparedStatement pst = conn.prepareStatement(sql);
                pst.setString(1, id);
                pst.execute();
                
                JOptionPane.showMessageDialog(this, "Data Berhasil Dihapus!");
                
                // Refresh tabel dan bersihkan form
                tampilkanData();
                bersihkanFormAdmin();
                
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Gagal Hapus: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

