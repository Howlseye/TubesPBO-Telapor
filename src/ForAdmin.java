    // Method khusus untuk mengambil data dari DB ke Tabel
    private void tampilkanData() {
        // 1. Siapkan header tabel
        String[] judulKolom = {"ID", "Pelapor", "Jenis", "Deskripsi", "Status", "Waktu Lapor"};
        DefaultTableModel model = new DefaultTableModel(null, judulKolom);
        tabelLaporan.setModel(model);

        try {
            Connection conn = KoneksiDB.configDB();
            Statement stm = conn.createStatement();
            
            // --- BAGIAN INI YANG BARU ---
            
            // Ambil kata kunci dari text field pencarian
            String keyword = txtCari.getText();
            
            String sql;
            // Cek: Jika kotak pencarian KOSONG, ambil semua data
            if (keyword.isEmpty()) {
                sql = "SELECT * FROM laporan ORDER BY id DESC";
            } 
            // Jika ada isinya, gunakan Filter WHERE LIKE
            else {
                // Simbol persen (%) artinya "cocokkan apa saja di depan/belakangnya"
                sql = "SELECT * FROM laporan WHERE nama_pelapor LIKE '%" + keyword + "%' OR deskripsi LIKE '%" + keyword + "%' ORDER BY id DESC";
            }
            
            // --- AKHIR BAGIAN BARU ---

            ResultSet res = stm.executeQuery(sql);

            // Looping data (sama seperti sebelumnya)
            while (res.next()) {
                Vector baris = new Vector();
                baris.add(res.getInt("id"));
                baris.add(res.getString("nama_pelapor"));
                baris.add(res.getString("jenis_laporan"));
                baris.add(res.getString("deskripsi"));
                baris.add(res.getString("status"));
                baris.add(res.getString("tanggal_input")); 
                model.addRow(baris);
            }
        } catch (Exception e) {
             JOptionPane.showMessageDialog(this, "Gagal Menampilkan Data:\n" + e.getMessage());
        }
    }
